create database if not exists bloquitocraft;

use bloquitocraft;

drop table if exists escenario_bioma;
drop table if exists bloquito;
drop table if exists construccion;
drop table if exists escenario;
drop table if exists bioma;
drop table if exists tipo;

create table if not exists tipo(
id_tipo int auto_increment primary key,
nombre enum("Tierra","Arena", "Madera", "Roca", "Agua", "Lava") not null,
peso_kg decimal(5,3) not null,
afecta_gravedad int not null
);

create table if not exists bioma(
id_bioma int auto_increment primary key,
nombre enum("Valle", "Bosque", "Monte rocoso", "Monte nevado", "Desierto") not null
);
create table if not exists escenario(
id_escenario int auto_increment primary key,
nombre varchar(255) not null,
modo enum("Creativo", "Supervivencia") not null,
fecha datetime not null
);
create table if not exists construccion(
id_construccion int auto_increment primary key,
nombre varchar(255),
fecha datetime
);
create table if not exists bloquito(
id_bloquito int auto_increment primary key,
pos_x int,
pos_y int,
pos_z int,
tipo_bloquito int not null,
id_escenario int not null,
id_construccion int,
foreign key(tipo_bloquito) references tipo(id_tipo),
foreign key(id_escenario) references escenario(id_escenario),
foreign key(id_construccion) references construccion(id_construccion)
);
create table if not exists escenario_bioma(
id_escenario_bioma int auto_increment primary key,
id_escenario int not null,
id_bioma int not null,
foreign key(id_escenario) references escenario(id_escenario),
foreign key(id_bioma) references bioma(id_bioma)
);

